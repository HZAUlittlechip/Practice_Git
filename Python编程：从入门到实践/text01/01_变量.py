# 变量的编辑【新的开始】
message = 'Hello World'
print(message)

# 变量可以字母和下划线打头
# 不可包含空格
# 使用小写字母来表示变量名
# python会对变量名进行检查而不会对代码进行检查

# 练习 2.1：简单消息 将⼀条消息赋给变量，并将其打印出来
message_for_mi = "LOVE YOU!"
print(message_for_mi)

# 练习 2.2：多条简单消息 将⼀条消息赋给变量，并将其打印出来；再
# 将变量的值修改为⼀条新消息，并将其打印出来。
message_for_mi = "LOVE YOU!"
print(message_for_mi)
message_for_mi = "I LOVE YOU SO MUCH!"
print(message_for_mi)
